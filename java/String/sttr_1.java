import java.util.*;

class sttr_1 {
	public static void main(String[] args) {
		String s = "Udit";
		System.out.println(s);
		System.out.println("---------------------------");
		System.out.println(s.length());
		// capital letter
		System.out.println(s.toUpperCase());
		// char at index 0
		System.out.println(s.charAt(0));
		// small letter
		System.out.println(s.toLowerCase());
	}
}